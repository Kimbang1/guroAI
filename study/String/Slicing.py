#슬라이싱 필요한 부분만 짤라오는 것
jumin = "990120-1234567"

print("성별 : "+jumin[7])
print("연 :"+ jumin[0:2])       #0부터 2직전까지 즉, 0,1번째 자리만 가져옴
print("월 :"+ jumin[2:4])
print("일 :"+ jumin[4:6])
print("생년월일 :"+ jumin[:6])  #:으로 시작하면 처음부터 6직전까지 


print("뒤 7자리 : "+ jumin[7:])  #7부터 끝까지 
print("뒤 7자리(뒤에부터) : "+ jumin[-7:])  #맨 뒤에서 7번째까지
