#파이선에서 제공하는 숫자처리 함수

print(abs(-5))      #5  abs=절댓값       
print(pow(4,2))     #4^2 = 4*4 = 16 pow=제곱
print(max(5,12))       #최대값
print(min(5,12))       #최소값
print(round(3.14))     #반올림

from math import * #math함수를 전부사용하겠다
print(floor(4.99))  #내림. 4
print(ceil(3.14))   #올림.4
print(sqrt(16))     #제곱근,4